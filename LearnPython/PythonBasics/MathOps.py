import math

# ceil
print(math.ceil(3.8))

# floor
print(math.floor(3.8))

# fabs
print(math.fabs(-7))

# factorial
print(math.factorial(5))

# comb
print(math.comb(5, 3))

# fmod
print(math.fmod(10, 3))

# gcd
print(math.gcd(48, 20))

# remainder
print(math.remainder(48, 20))       

# exp
print(math.exp(1))                  # Return e raised to the power x, where e = 2.718281

# log
print(math.log(math.exp(1)))        # natural logarithm of x (to base e)

# pow
print(math.pow(5, 3))               # Return x raised to the power y

# sqrt
print(math.sqrt(625))               # Return the square root of x

# dist
print(math.dist((1, 2), (4, 6)))    # Return the Euclidean distance between two points p and q

# degrees
print(math.degrees(math.pi))        # Convert angle x from radians to degrees

# radians
print(math.radians(180))            # Convert angle x from degrees to radians